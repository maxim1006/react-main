function countProduct(arr) {
    if (arr.toString() in store) return store[arr];

    store[arr] = arr.reduce((acc, i) => acc * i, 1);

    return store[arr];
}

function purgeArr(win, k) {
    if (win.toString() in counterStore) counterStore[win];

    let counter = 0;

    while (win.length > 2) {
        win.shift();

        if (countProduct(win) < k) {
            // res.push([...win]);
            ++counter;
        }
    }

    return counter;
}

