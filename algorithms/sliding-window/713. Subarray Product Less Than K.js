let store = {};
let counterStore = {};

var numSubarrayProductLessThanK = function (nums, k) {
    if (k <= 1) return 0;

    let prod = 1;
    let left = 0;
    let counter = 0;

    for (let right = 0; right < nums.length; right++) {
        prod *= nums[right];

        while (prod > k) {
            prod /= nums[left];
            left++;
        }

        counter += right - left + 1;
    }

    return counter;
};

